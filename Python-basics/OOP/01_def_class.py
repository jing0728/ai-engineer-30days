"""
案例：演示定义汽车类 以及 使用类中的成员

面向对象核心概念：
    类：抽象的概念，看不见，摸不着，是属性（名词）和行为（动词）的集合
    对象：类的具体体现，实现
    属性（名词）：用来描述事物的外资特征的，例如：名字，年龄。。。
        格式：和以前定义变量一样
    行为（动词）：用来描述事物能够做什么的，例如：吃，喝。。。。
        格式：和以前定义函数一样
定义类的格式：
    class 类名：
        #属性
        #行为

需求：定义汽车类，有跑的行为

如何访问类中的成员
    step1：创建该类对象
        对象名=类名（）
    step2：通过 对象名. 的方法进行调用
        对象名.属性名
        对象名.行为名
"""

#1.定义汽车类
class Car:  #类名遵循 大驼峰的命名法
    #属性

    #行为
    def run(self):
        print("car run")

"""
对象
对象的基本语法格式
    python对象实例化：对象名=类（）
    调用方法：对象名.方法名（）
"""
#创建对象：对象名 =类名（）--- 创建汽车类的对象
car = Car()
#调用行为：对象名.方法名 --- 调用Car#run()
car.run()